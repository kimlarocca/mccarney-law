                <p><em>by Andrew Edgecliffe - Johnson</em></p>
                <p><strong>TI GROUP YESTERDAY</strong>, settled the "whistleblower" legal action that has hung over the
                    engineering group for five years by paying $12.4m (£ 7.5m) to the US government. </p>
                <p> Jeffrey Thistlethwaite, a former employee of Dowty Woodville Polymer, had claimed that the
                    subsidiary, which TI acquired in its 1992 purchase of Dowty Group, had overcharged the US government
                    by $20m for seals used in wing slots of F-111 and B1 bombers between 1984 and 1995. </p>
                <p> The action, which was settled yesterday with no admission of liability, was begun in the spring of
                    1993, formally filed in May 1994, and joined in 1995 by the US government. Lawyers for Mr.
                    Thistlethwaite had estimated that damages could have reached $60m. </p>
                <p> The case was the first in which US authorities had joined a foreign citizen in a civil action
                    against a foreign manufacturer where the allegations concerned activities on foreign soil. </p>
                <p> No decision has yet been taken on how much Mr. Thistlethwaite will receive personally, but US
                    "whistleblower" legislation allows for payment of 15-20 percent of the total to the individual. This
                    suggests a payment of $1.85-3.1m to Mr. Thistlethwaite, who left Dowty in 1992 before TI's
                    acquisition. </p>
                <p> Mr. Thistlethwaite would only say last night that he was "happy that the case has been resolved to
                    the satisfaction of all parties". </p>
                <p> TI said it found the terms of the settlement attractive, and had accepted them "in order to avoid
                    the future cost and management diversion involved in litigation, and to achieve certainly of
                    outcome". </p>
                <p> David Lillycrop, legal counsel for TI, said the group would also pay some of the other side's legal
                    costs, including " an incidental contribution " of " a few hundred thousand dollars" towards Mr.
                    Thistlethwaite's legal fees. TI's own legal costs had reached about £2m by the start of the year,
                    but had begun to grow more rapidly, he added. </p>
                <p> Mr. Thistlethwaite's lawyer, Thomas Engel of Engel and McCarney in New York said: "Their willingness
