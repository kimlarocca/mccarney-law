                <h2 class="fancy"><span class="dark-gray">08.12.04</span><br>Citigroup's Travelers Settles Medicare
                    Fraud Lawsuit</h2>
                <p><em>by David Glovin</em></p>
                <p><strong>Citigroup Inc.'s Travelers Insurance Co. and United Healthcare Insurance Co.</strong> agreed
                    to pay $20.6 million to settle a civil suit by the U.S. government accusing them of cheating the
                    federal Medicare program.</p>
                <p> The allegations were brought to the government by a former Travelers employee, Robert Klimasewiski,
                    who worked for the company from 1978 to 1995. He made the claims in his own suit, which the
                    government later adopted, court papers say.</p>
                <p> The 1999 suit claimed the two companies, which helped administer Medicare in several states,
                    defrauded the federal health care program for people over 65. According to the suit, Travelers and
                    United Healthcare got excessive reimbursements from the government by over-billing for care provided
                    by doctors and hospitals.</p>
                <p> The government's investigation revealed that Travelers kept two sets of books: its actual costs and
                    costs as reported to the government, interim U.S. Attorney David Kelley said in a statement.</p>
                <p> Under the settlement, Travelers, the insurance unit of the world's biggest financial services
                    company, will pay $10.9 million. United Healthcare, a division of UnitedHealth Group, Inc., the
                    biggest U.S. insurer, will pay $9.7 million.</p>
                <p> Neither Travelers nor Minnetonka, Minnesota-based United Healthcare admitted wrongdoing. Travelers,
                    which is headquartered in New York, said in a statement that it settled the case Ato avoid
                    distraction of further litigation.</p>
                <p> A United Healthcare spokesman declined to comment.</p>
                <p> Klimasewiski said in his complaint that the scheme began as early as 1978 and continued after United
                    Healthcare assumed the Travelers Medicare obligations in 1995. He says Travelers promised to provide
                    cost savings and eliminate waste and instead committed the very acts they were hired to police and
                    extirpate.</p>
                <p> Klimasewiski made use of a law that lets individuals sue on the government's behalf for a violation
                    of the federal False Claims Act. The government subsequently adopted the suit as its own.
                    Klimasewiski will get a share of the settlement.</p>
                <p> This is further vindication of the way whistle-blowers end up contributing to the efficiency of
                    government programs, his lawyer, Thomas Engel, said.</p>
                <p> The suit originally alleged that the government was cheated out of more than $700 million. That
                    amount was later reduced.</p>
                <p> The case is U.S. ex rel. Klimasewiski v. UnitedHealth Group, 99 cv 11139, Southern District of New
                    York.</p>
